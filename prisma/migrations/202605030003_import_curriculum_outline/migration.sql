INSERT INTO public.lessons (
  chapter_id,
  title,
  content_type,
  content_url,
  simulator_config,
  legacy_lesson_id,
  full_id,
  lesson_type,
  sim_content,
  content_text,
  order_index,
  is_active
)
SELECT
  chapter.id,
  lesson.title,
  CASE WHEN lesson.lesson_type IN ('播放','情境','觀念','補充') THEN 'text' ELSE 'simulation' END,
  '/' || lesson.chapter_key,
  jsonb_build_object(
    'source', 'curriculum_outline',
    'type', lesson.lesson_type,
    'prompt', lesson.title,
    'simulation', lesson.sim_content,
    'keyword', lesson.content_text
  ),
  lesson.legacy_lesson_id,
  lesson.full_id,
  lesson.lesson_type,
  lesson.sim_content,
  lesson.content_text,
  lesson.order_index,
  TRUE
FROM lesson_seed lesson
JOIN upserted_chapters chapter ON chapter.chapter_key = lesson.chapter_key
ON CONFLICT (chapter_id, legacy_lesson_id) WHERE legacy_lesson_id IS NOT NULL DO UPDATE
SET title = EXCLUDED.title,
    content_type = EXCLUDED.content_type,
    content_url = EXCLUDED.content_url,
    simulator_config = EXCLUDED.simulator_config,
    full_id = EXCLUDED.full_id,
    lesson_type = EXCLUDED.lesson_type,
    sim_content = EXCLUDED.sim_content,
    content_text = EXCLUDED.content_text,
    order_index = EXCLUDED.order_index,
    is_active = EXCLUDED.is_active;
